    private void KocokAngkaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_KocokAngkaActionPerformed
        clip3.play();
        Thread kocokrun = new Thread(){
            @Override
            public void run(){
                while(true){

                    if(Kocokswitch == 1){
                        game.setAng1((int)(Math.random()*9));
                        game.setAng2((int)(Math.random()*9));
                       
                        ui.setTampilAngka(Angka_1,Angka1,game.getAng1());
                        ui.setTampilAngka(Angka_2,Angka2,game.getAng2());
                    }

                    try{
                        sleep(100);
                    } catch(InterruptedException ex){

                    }
                }

            }
        };

        if(Kocokswitch==1){
            Kocokswitch = 0;
            kocokrun.suspend();
            if(game.getAng1()>game.getAng2()){
                game.setGiliran(1);
                konfirm.setVisible(false);
                P1_first.setVisible(true);
                KocokAngka.setVisible(false);
                Next.setVisible(true);
            }else if(game.getAng2()>game.getAng1()){
                game.setGiliran(2);
                konfirm.setVisible(false);
                P2_first.setVisible(true);
                KocokAngka.setVisible(false);
                Next.setVisible(true);
            }else{
                konfirm.setVisible(false);
                angka_sama.setVisible(true);
            }

        }
        else{
            angka_sama.setVisible(false);
            konfirm.setVisible(true);
            Kocokswitch = 1;
            if(isKocok){
                kocokrun.resume();
            }
            else{
                kocokrun.start();
                isKocok=true;
            }

        }
    }//GEN-LAST:event_KocokAngkaActionPerformed

