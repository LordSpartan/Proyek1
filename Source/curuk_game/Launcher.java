    private String getHand(int x,int y){
        String TmpHand = "kosong";
        
        if((x>=46 && x<=402 ) && ( y>=199 && y<=330)){
            TmpHand = "P1Left";
        }else if((x>=46 && x<=402  )&& ( y>=380 && y<=509)){
            TmpHand = "P1Right";
        }else if((x>=547  && x<=910) && ( y>=199 && y<=330  )){
            TmpHand = "P2Right";
        }else if((x>=547 && x<=910)  && ( y>=380 && y<=509  )){
            
            TmpHand = "P2Left";
        }
        return TmpHand;
    }

