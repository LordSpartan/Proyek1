    private void chooseFirstHand(int x,int y){
        game.setClickHand1(getHand(x,y));
        if(isFirstHandValid() == true){
            if(game.getGiliran()==1){
                switch (game.getClickHand1()){
                    case "P1Left":
                        ui.setTampilTangan_h(P1Kiri_h,P1.SkinKiri_h,P1.getKiri());
                        ui.setEnable(P1Kiri_h);
                        break;
                    case "P1Right":
                        ui.setTampilTangan_h(P1Kanan_h,P1.SkinKanan_h,P1.getKanan());                        
                        ui.setEnable(P1Kanan_h);
                        break;
                    default:
                        game.setClickHand1("kosong");
                        break;
                }
            } else {
                switch (game.getClickHand1()){
                    case "P2Right":
                        ui.setTampilTangan_h(P2Kanan_h,P2.SkinKanan_h,P2.getKanan());                        
                        ui.setEnable(P2Kanan_h);
                        break;
                    case "P2Left":
                        ui.setTampilTangan_h(P2Kiri_h,P2.SkinKiri_h,P2.getKiri());
                        ui.setEnable(P2Kiri_h);
                        break;
                    default:
                        game.setClickHand1("kosong");
                        break;
                }
            }
        }
        else{
            game.setClickHand1("kosong");               
        }
    }
    
    //mengecek apakah tangan yang dipilih pertama terdapat dalam kordinat tertentu
    private boolean isFirstHandValid(){
        if ((game.getClickHand1().equals("P1Left")&&P1.getKiri()>0)||
            (game.getClickHand1().equals("P1Right")&&P1.getKanan()>0)||
            (game.getClickHand1().equals("P2Left")&&P2.getKiri()>0)||
            (game.getClickHand1().equals("P2Right")&&P2.getKanan()>0)){
            return true;
        }
        return false;
    }
    
    //mengecek apakah tangan yang dipilih kedua terdapat dalam kordinat tertentu
    private boolean isSecondHandValid(){
        if ((game.getClickHand2().equals("P1Left")&&((P1.getKiri()>0)||(game.getClickHand1().equals("P1Right")&&P1.getNyawa()!=0)))||
            (game.getClickHand2().equals("P1Right")&&((P1.getKanan()>0)||(game.getClickHand1().equals("P1Left")&&P1.getNyawa()!=0)))||
            (game.getClickHand2().equals("P2Left")&&((P2.getKiri()>0)||(game.getClickHand1().equals("P2Right")&&P2.getNyawa()!=0)))||
            (game.getClickHand2().equals("P2Right")&&((P2.getKanan()>0)||(game.getClickHand1().equals("P2Left")&&P2.getNyawa()!=0)))){
            return true;
        }
        return false;
    }
    
