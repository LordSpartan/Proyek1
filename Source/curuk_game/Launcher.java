    private void BackgroundMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_BackgroundMouseClicked
        int koor_X=evt.getX();
        int koor_Y=evt.getY();
        System.out.println(koor_X +" "+ koor_Y);
        // Klik Tangan Pertama 
        if(game.getClickHand1().equals("kosong")){
            chooseFirstHand(koor_X,koor_Y);
        }
        else{  //Klik Tangan Kedua 
            chooseSecondHand(koor_X,koor_Y);
            if(game.getClickHand1().equals(game.getClickHand2())){ // Jika Tangan Ke-1 sama dengan Tangan ke-2 maka kembali ke state awal (kosong)
                ui.setDisable(P1Kiri_h);
                ui.setDisable(P1Kanan_h);
                ui.setDisable(P2Kanan_h);
                ui.setDisable(P2Kiri_h);
                game.setClickHand1("kosong");
                game.setClickHand2("kosong");
            }else if(isSecondHandValid()){  //Lakukan Turn 
                playerChoice();
                //refresh();
            }
        }
        System.out.println(game.getClickHand1()+ " " + game.getClickHand2());
        
    }//GEN-LAST:event_BackgroundMouseClicked

